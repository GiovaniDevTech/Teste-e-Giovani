//Encontre de 3 a 5 comparações que o resultado é Falso ou False

//Operadores Relacionais 
// >, <, <=, >=, ==, ===, !=

//Exemplo:

console.log((3 > 5)) // Falso
console.log( 3 < 5) // Verdadeiro
console.log( 3 <= 5) // Verdadeiro
console.log( 3 >= 5) // Falso
console.log( 3 == 5) // Falso
console.log( 3 === 5) // Falso
console.log( 3 != 5) // Verdadeiro